"""
CLI 入口 for v2.0.

Usage:
  python -m agents_chat.v2.main init [--data-dir DIR]
  python -m agents_chat.v2.main run-agent AGENT_ID [--cli mock|qwen|opencode] [--data-dir DIR]
  python -m agents_chat.v2.main run-scanner [--data-dir DIR]
  python -m agents_chat.v2.main run-scheduler [--data-dir DIR]
  python -m agents_chat.v2.main run-all [--data-dir DIR]    # dev only
  python -m agents_chat.v2.main post CHANNEL CONTENT         # helper, 用 'from' = god
  python -m agents_chat.v2.main status [TASK_ID]             # 看 state_board
  python -m agents_chat.v2.main tail CHANNEL [N]            # 看频道最后 N 条 (默认 10)
  python -m agents_chat.v2.main inbox AGENT_ID               # 看 agent 邮箱 pending
  python -m agents_chat.v2.main reset                        # 清空所有 data (危险)

默认 data_dir: ./data_v2 (可用 AGENTS_CHAT_DATA_DIR 环境变量覆盖)
"""
from __future__ import annotations

import argparse
import asyncio
import json
