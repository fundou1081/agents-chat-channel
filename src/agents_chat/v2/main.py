async def _run_all_async(args):
    """开发用: 一起跑 scanner + scheduler + 多个 agent (subprocess 或 task)."""
    data_dir = get_data_dir(args)
    ensure_dirs(data_dir)
    # 注册默认 agent mailboxes
    for aid in args.agents:
        mb = data_dir / "mailboxes" / f"{aid}.json"
        if not mb.exists():
            Mailbox(mb, aid)

    scanner = Scanner(data_dir=data_dir, scan_interval=1.0)
    scheduler = Scheduler(
        data_dir=data_dir, stale_ttl=60, grace_period=30, check_interval=15,
    )
    agents = []
    for aid in args.agents:
        cli = make_cli(args.cli, aid, data_dir)
        agents.append(Agent(
            agent_id=aid, cli=cli, data_dir=data_dir, poll_interval=2.0,
        ))

    tasks = [asyncio.create_task(scanner.run())]
    for a in agents:
        tasks.append(asyncio.create_task(a.run()))
    tasks.append(asyncio.create_task(scheduler.run()))

    print(f"[run-all] started: scanner + scheduler + {len(agents)} agent(s)")
    try:
        await asyncio.gather(*tasks)
    except KeyboardInterrupt:
        scanner.stop(); scheduler.stop()
        for a in agents: a.stop()
        await asyncio.gather(*tasks, return_exceptions=True)


def cmd_run_all(args):
    asyncio.run(_run_all_async(args))


def cmd_post(args):
    data_dir = get_data_dir(args)
    ensure_dirs(data_dir)
    ch = Channel(data_dir / "channels" / f"{args.channel}.jsonl", args.channel)
    # 自动从 content 提取 mentions
    from .agent import extract_mentions
    mentions = extract_mentions(args.content)
    # 自动检测 [TASK] 标记
    import re
    msg_type = "task_broadcast" if re.search(r"\[TASK", args.content, re.IGNORECASE) else "mention"
    msg_id = ch.append(
        from_=args.sender,
        content=args.content,
        type=msg_type,
        mentions=mentions,
    )
    print(f"✓ Posted: {msg_id}  (channel={args.channel}, mentions={mentions}, type={msg_type})")


def cmd_status(args):
    data_dir = get_data_dir(args)
    sb = StateBoard(data_dir / "state_board.json")
    if args.task_id:
        e = sb.get(args.task_id)
        if not e:
            print(f"task {args.task_id} not found")
            return
        print(json.dumps(e, ensure_ascii=False, indent=2))
    else:
        all_tasks = sb.list_all()
        if not all_tasks:
            print("(empty)")
            return
        for tid, e in all_tasks.items():
            print(
                f"  {tid:25s} {e.get('agent', '?'):15s} "
                f"progress={e.get('progress', 0):3d}  "
                f"hb={e.get('heartbeat', '?')[:19]}"
            )


def cmd_tail(args):
    data_dir = get_data_dir(args)
    ch = Channel(data_dir / "channels" / f"{args.channel}.jsonl", args.channel)
    msgs = ch.tail(args.n)
    if not msgs:
        print(f"(empty channel: {args.channel})")
        return
    for m in msgs:
        ts = m.get("ts", "")[:19]
        frm = m.get("from", "?")
        ctype = m.get("type", "?")
        content = m.get("content", "")[:200].replace("\n", " | ")
        print(f"  [{ts}] {frm:15s} ({ctype:12s}) {content}")


def cmd_inbox(args):
    data_dir = get_data_dir(args)
    mb = Mailbox(data_dir / "mailboxes" / f"{args.agent_id}.json", args.agent_id)
    pending = mb.peek()
    if not pending:
        print(f"(empty inbox: {args.agent_id})")
        return
    for m in pending:
        ts = m.get("ts", "")[:19]
        mtype = m.get("type", "?")
        content = m.get("content", "")[:200].replace("\n", " | ")
        print(f"  [{ts}] ({mtype:14s}) {content}")


def cmd_reset(args):
    """危险: 清空 data_dir."""
    data_dir = get_data_dir(args)
    if not data_dir.exists():
        print(f"data dir {data_dir} 不存在, 无事可做")
        return
    if not args.yes:
        resp = input(f"⚠️  确认清空 {data_dir}? (yes/no): ")
        if resp.strip().lower() != "yes":
            print("已取消")
            return
    import shutil
    shutil.rmtree(data_dir)
    print(f"✓ Reset: {data_dir} 已清空")


