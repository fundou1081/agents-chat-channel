# Subcommands
# ============================================================================


def cmd_init(args):
    """初始化 data_dir."""
    data_dir = get_data_dir(args)
    ensure_dirs(data_dir)
    # 写 README
    readme = data_dir / "README.md"
    if not readme.exists():
        readme.write_text(
            f"# agents-chat-channel v2.0 data\n\n"
            f"Generated by `python -m agents_chat.v2.main init` at {time.ctime()}\n\n"
            f"Layout:\n"
            f"  channels/     # JSONL 频道 (每行一条消息)\n"
            f"  mailboxes/    # JSON 邮箱 (每个 agent 一个文件)\n"
            f"  sessions/     # JSON session 索引 (local → remote)\n"
            f"  locks/        # 任务认领锁 (task_xxx.lock)\n"
            f"  state_board.json    # 全局任务状态板\n"
            f"  scanner_state.json  # Scanner 各频道 offset\n"
            f"  scheduler_state.json # Scheduler request_log\n"
        )
    # 默认频道
    (data_dir / "channels" / "general.jsonl").touch()
    # 主动建 state 文件 (StateBoard / Scanner / Scheduler 是懒加载, init 时主动建)
    StateBoard(data_dir / "state_board.json")
    (data_dir / "scanner_state.json").write_text('{"offsets": {}, "updated_at": ""}')
    (data_dir / "scheduler_state.json").write_text('{"request_log": {}, "updated_at": ""}')
    print(f"✓ Initialized: {data_dir}")
    print(f"  Layout: channels/ mailboxes/ sessions/ locks/")
    print(f"  State: state_board.json scanner_state.json scheduler_state.json")


def make_cli(cli_name: str, agent_id: str, data_dir: Path):
    """根据 cli name 构造 CLI 实例."""
    if cli_name == "mock":
        return MockCLI()
    elif cli_name == "opencode":
        return OpenCodeCLI()
    elif cli_name == "qwen":
        history_dir = data_dir / "qwen_history"
        history_dir.mkdir(parents=True, exist_ok=True)
        return QwenCLI(history_dir=history_dir)
    else:
        raise ValueError(f"unknown CLI: {cli_name}")


async def _run_agent_async(args):
    data_dir = get_data_dir(args)
    ensure_dirs(data_dir)
    cli = make_cli(args.cli, args.agent_id, data_dir)
    agent = Agent(
        agent_id=args.agent_id,
        cli=cli,
        data_dir=data_dir,
        capabilities=args.capabilities or [],
        poll_interval=args.poll_interval,
        default_channel=args.channel,
    )
    try:
        await agent.run()
    except KeyboardInterrupt:
        agent.stop()


def cmd_run_agent(args):
    asyncio.run(_run_agent_async(args))


