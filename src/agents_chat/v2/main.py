def build_parser() -> argparse.ArgumentParser:
    p = argparse.ArgumentParser(
        prog="python -m agents_chat.v2.main",
        description="agents-chat-channel v2.0 CLI",
    )
    p.add_argument("--data-dir", default=None, help=f"data dir (default: {DEFAULT_DATA_DIR} 或 $AGENTS_CHAT_DATA_DIR)")
    sub = p.add_subparsers(dest="cmd", required=True)

    def add_common(sp):
        sp.add_argument("--data-dir", default=None, dest="_data_dir", help="(同顶层)")

    # init
    sp = sub.add_parser("init", help="初始化 data_dir")
    add_common(sp)

    # run-agent
    sp = sub.add_parser("run-agent", help="跑一个 Agent 进程")
    add_common(sp)
    sp.add_argument("agent_id")
    sp.add_argument("--cli", default="mock", choices=["mock", "opencode", "qwen"])
    sp.add_argument("--capabilities", nargs="*", default=[])
    sp.add_argument("--poll-interval", type=float, default=2.0)
    sp.add_argument("--channel", default="general")

    # run-scanner
    sp = sub.add_parser("run-scanner", help="跑 Scanner 后台进程")
    add_common(sp)
    sp.add_argument("--scan-interval", type=float, default=1.0)

    # run-scheduler
    sp = sub.add_parser("run-scheduler", help="跑 Scheduler 后台进程")
    add_common(sp)
    sp.add_argument("--stale-ttl", type=int, default=300)
    sp.add_argument("--grace-period", type=int, default=60)
    sp.add_argument("--check-interval", type=float, default=30.0)

    # run-all (dev)
    sp = sub.add_parser("run-all", help="一起跑: scanner + scheduler + 多 agent")
    add_common(sp)
    sp.add_argument("--agents", nargs="+", default=["qwencode", "claude"])
    sp.add_argument("--cli", default="mock", choices=["mock", "opencode", "qwen"])

    # post
    sp = sub.add_parser("post", help="发消息到频道 (helper)")
    add_common(sp)
    sp.add_argument("channel")
    sp.add_argument("content")
    sp.add_argument("--sender", default="god")

    # status
    sp = sub.add_parser("status", help="看 state_board")
    add_common(sp)
    sp.add_argument("task_id", nargs="?", default=None)

    # tail
    sp = sub.add_parser("tail", help="看频道最后 N 条")
    add_common(sp)
    sp.add_argument("channel")
    sp.add_argument("--n", type=int, default=10)

    # inbox
    sp = sub.add_parser("inbox", help="看 agent 邮箱 pending")
    add_common(sp)
    sp.add_argument("agent_id")

    # reset
    sp = sub.add_parser("reset", help="⚠️  清空 data_dir")
    add_common(sp)
    sp.add_argument("--yes", action="store_true")

    return p


def main(argv: list[str] | None = None):
    parser = build_parser()
    args = parser.parse_args(argv)

    dispatch = {
        "init": cmd_init,
        "run-agent": cmd_run_agent,
        "run-scanner": cmd_run_scanner,
        "run-scheduler": cmd_run_scheduler,
        "run-all": cmd_run_all,
        "post": cmd_post,
        "status": cmd_status,
        "tail": cmd_tail,
        "inbox": cmd_inbox,
        "reset": cmd_reset,
    }
    dispatch[args.cmd](args)


if __name__ == "__main__":
    main()
