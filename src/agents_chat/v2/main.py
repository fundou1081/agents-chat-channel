from typing import Optional

from .agent import Agent
from .cli.mock import MockCLI
from .cli.opencode import OpenCodeCLI
from .cli.qwen import QwenCLI
from .files.channel import Channel
from .files.mailbox import Mailbox
from .scanner import Scanner
from .scheduler import Scheduler
from .state_board import StateBoard


DEFAULT_DATA_DIR = "./data_v2"


def get_data_dir(args) -> Path:
    p = (getattr(args, "_data_dir", None)
         or getattr(args, "data_dir", None)
         or os.environ.get("AGENTS_CHAT_DATA_DIR")
         or DEFAULT_DATA_DIR)
    return Path(p).resolve()


def ensure_dirs(data_dir: Path):
    """建子目录."""
    for sub in ["channels", "mailboxes", "sessions", "locks"]:
        (data_dir / sub).mkdir(parents=True, exist_ok=True)


