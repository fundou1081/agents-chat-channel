    @pytest.mark.asyncio
    async def test_timeout_failure(self, tmp_path: Path):
        """Stage 超时 → workflow 失败, failed_stage 记录."""
        data_dir = tmp_path / "data_v2"
        init_data_v2(data_dir)

        spec = load_workflow_from_string(E2E_WORKFLOW_YAML)
        for s in spec.stages:
            s.timeout = 1  # 极短 timeout

        scheduler = WorkflowScheduler(spec, data_dir)

        # 不产出 deliverable → 全部 timeout
        result = await scheduler.run()

        assert result.status == "failed"
        assert result.failed_stage is not None  # research timeout
        # research 失败
        assert result.stage_states.get("research") == "failed"

