class TestE2E3StagePipeline:
    """完整的 3-stage pipeline 端到端测试."""

    @pytest.mark.asyncio
    async def test_full_success(self, tmp_path: Path):
        """3 stage 全部成功."""
        data_dir = tmp_path / "data_v2"
        init_data_v2(data_dir)

        spec = load_workflow_from_string(E2E_WORKFLOW_YAML)
        for s in spec.stages:
            s.timeout = 5

        scheduler = WorkflowScheduler(spec, data_dir)

        # 模拟 workers 产出
        async def mock_produce():
            # Stage research: 0.5s 后产出
            await asyncio.sleep(0.5)
            write_deliverable(data_dir, "out/research.json", RESEARCH_OUTPUT)
            # Stage write: 2s 后产出
            await asyncio.sleep(2.0)
            write_deliverable(data_dir, "out/report.md", WRITE_OUTPUT)
            # Stage publish: 2s 后产出
            await asyncio.sleep(2.0)
            write_deliverable(data_dir, "out/final.md", PUBLISH_OUTPUT)

        scheduler_task = asyncio.create_task(scheduler.run())
        mock_task = asyncio.create_task(mock_produce())

        result = await scheduler_task
        if not mock_task.done():
            mock_task.cancel()

        assert result.status == "success", f"failed_stage={result.failed_stage}"
        assert result.stage_states == {
            "research": "success",
            "write": "success",
            "publish": "success",
        }

